package com.harugasumi.core;

public class LinkEntries {
	 private String level;
	 private String url;
	 
	 public LinkEntries() {
	 }
	 
	 public LinkEntries(String level, String url) {
		 this.level = level;
		 this.url = url;
	 }
	 
	 public String getLevel() {
		 return this.level;
	 }
	 
	 public String getURL() {
		 return this.url;
		 
	 }
	 
	 
}
